    public void cargarReporte() {

        Connection conn = null;
        PreparedStatement ps = null;
        ResultSet rs = null;

        try {
            conn = Conexion.conectar();

            String sql = "SELECT DATE(fecha) as fecha, COUNT(*) as cantidad, SUM(total) as total FROM ventas GROUP BY DATE(fecha)";

            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();

            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("Fecha");
            modelo.addColumn("Cantidad Ventas");
            modelo.addColumn("Total");
          

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getString("fecha"),
                    rs.getInt("cantidad"),
                    rs.getDouble("total")
                });
            }

            tablaReportes.setModel(modelo);

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (rs != null) rs.close();
                if (ps != null) ps.close();
                if (conn != null) conn.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }
