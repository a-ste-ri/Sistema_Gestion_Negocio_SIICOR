    private void cargarVentasPorFecha(String inicio, String fin) {

    Connection conn = null;
    PreparedStatement ps = null;
    ResultSet rs = null;

    try {

        // 🔒 VALIDAR FORMATO ANTES DE CONSULTAR
        if (!inicio.matches("\\d{4}-\\d{2}-\\d{2}") ||
            !fin.matches("\\d{4}-\\d{2}-\\d{2}")) {

            JOptionPane.showMessageDialog(this, "Formato inválido (YYYY-MM-DD)");
            return;
        }

        conn = Conexion.conectar();

        String sql = "SELECT * FROM ventas WHERE fecha BETWEEN ? AND ?";
        ps = conn.prepareStatement(sql);

        ps.setString(1, inicio + " 00:00:00");
        ps.setString(2, fin + " 23:59:59");

        rs = ps.executeQuery();

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Venta");
        modelo.addColumn("Fecha");
        modelo.addColumn("Total");

        double totalGeneral = 0;
        boolean hayDatos = false; // 🔥 CONTROL REAL

        while (rs.next()) {

            hayDatos = true;

            int id = rs.getInt("id");
            String fecha = rs.getString("fecha");
            double total = rs.getDouble("total");

            totalGeneral += total;

            modelo.addRow(new Object[]{id, fecha, total});
        }

        tablaReportes.setModel(modelo);
        lblTotal.setText("Total vendido: " + totalGeneral);

        // 🔥 AQUÍ VA EL MENSAJE
        if (!hayDatos) {
            JOptionPane.showMessageDialog(this, "No hay ventas en ese rango");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}
    private void topProductos() {

    Connection conn = null;
    PreparedStatement ps = null;
    ResultSet rs = null;

    try {
        conn = Conexion.conectar();

        String sql = "SELECT p.nombre, SUM(d.cantidad) as total_vendido " +
                     "FROM detalle_venta d " +
                     "JOIN productos p ON d.id_producto = p.id " +
                     "GROUP BY p.nombre " +
                     "ORDER BY total_vendido DESC";

        ps = conn.prepareStatement(sql);
        rs = ps.executeQuery();

        String mensaje = "TOP PRODUCTOS:\n\n";

        while (rs.next()) {
            mensaje += rs.getString("nombre") + " → " +
                       rs.getInt("total_vendido") + "\n";
        }

        JOptionPane.showMessageDialog(this, mensaje);

    } catch (Exception e) {
        e.printStackTrace();
    }
}

